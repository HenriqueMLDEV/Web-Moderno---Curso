function operacaoValores(a, b) {
  console.log("A soma dos parametros passados é " + (a + b));
  console.log("A subtração dos parametros passados é " + (a - b));
  console.log("A multiplicação dos parametros passados é " + a * b);
  console.log("A divisão dos parametros passados é " + a / b);
}

operacaoValores(4, 2);
